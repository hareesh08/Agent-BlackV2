def shell(client):
    """Open an interactive SSH shell with useful aliases."""
    print(f">>> Opening SSH shell to {USER}@{HOST}")
    print(f"    Quick commands:")
    print(f"      cd {APP_DIR} && docker compose ps")
    print(f"      cd {APP_DIR} && docker compose logs -f")
    print(f"      cd {APP_DIR} && docker compose restart")
    print()

    channel = client.get_transport().open_session()
    channel.get_pty(width=120, height=40)
    channel.invoke_shell()

    import select
    try:
        while True:
            r, w, e = select.select([channel, sys.stdin], [], [], 1)
            if channel in r:
                data = channel.recv(4096)
                if not data:
                    break
                sys.stdout.buffer.write(data)
                sys.stdout.flush()
            if sys.stdin in r:
                data = sys.stdin.buffer.read1(4096)
                if not data:
                    break
                channel.send(data)
    except (EOFError, KeyboardInterrupt):
        pass
    finally:
        channel.close()


