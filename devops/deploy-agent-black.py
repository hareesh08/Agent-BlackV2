def main():
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")
    sys.stderr.reconfigure(encoding="utf-8", errors="replace")

    print("=" * 60)
    print("  AGENT BLACK — REMOTE DEPLOYMENT MANAGER (GHCR)")
    print("=" * 60)
    print(f"  Server   : {USER}@{HOST}")
    print(f"  Domain   : {DOMAIN}")
    print(f"  Images   : {IMAGE_BASE}/*:{IMAGE_TAG}")
    print(f"  App Dir  : {APP_DIR}")
    print("=" * 60)
    print()
    print("  1) setup          - Full deploy (Docker, Nginx, SSL, pull images)")
    print("  2) status         - Check server & container status")
    print("  3) update         - Pull latest GHCR images & restart (fast!)")
    print("  4) restart        - Restart all services")
    print("  5) logs           - View recent container logs")
    print("  6) ssl-renew      - Renew SSL certificate")
    print("  7) rollback       - Show image tags for manual rollback")
    print("  8) fresh-rebuild  - Nuke everything & clean pull")
    print("  9) shell          - Open interactive SSH shell")
