def setup(client):
    """Full fresh deploy: Docker + Nginx + SSL + pull GHCR images."""

    # Step 1: Install Docker, Nginx, Certbot
    print(">>> Installing Docker, Nginx, Certbot...")
    bootstrap = textwrap.dedent("""\
        set -euo pipefail
        export DEBIAN_FRONTEND=noninteractive

        apt update
        apt install -y ca-certificates curl gnupg lsb-release openssl git ufw

        # Docker repo
        install -m 0755 -d /etc/apt/keyrings
        if [ ! -f /etc/apt/keyrings/docker.gpg ]; then
          curl -fsSL https://download.docker.com/linux/ubuntu/gpg | gpg --dearmor -o /etc/apt/keyrings/docker.gpg
          chmod a+r /etc/apt/keyrings/docker.gpg
        fi
        cat >/etc/apt/sources.list.d/docker.list <<EOF
        deb [arch=$(dpkg --print-architecture) signed-by=/etc/apt/keyrings/docker.gpg] https://download.docker.com/linux/ubuntu $(. /etc/os-release && echo "$VERSION_CODENAME") stable
        EOF
        apt update
        apt install -y docker-ce docker-ce-cli containerd.io docker-compose-plugin
        systemctl enable --now docker

        apt install -y nginx
        systemctl enable --now nginx

        apt install -y certbot python3-certbot-nginx
    """)
    run(client, bootstrap, timeout=600)

    # Step 2: Login to GHCR (for private repos)
    if GHCR_TOKEN:
        print(">>> Logging into GHCR...")
        run(client, f'echo "{GHCR_TOKEN}" | docker login ghcr.io -u "{GITHUB_OWNER}" --password-stdin')

    # Step 3: Auto-detect server IP
    server_ip = detect_server_ip(client)

    # Step 4: Write config files
    print(">>> Writing docker-compose.yml...")
    run(client, f"mkdir -p {APP_DIR}")
    write_remote_file(client, f"{APP_DIR}/docker-compose.yml", generate_compose_file())

    print(f">>> Writing .env file (VITE_API_URL auto-set for {server_ip})...")
    write_remote_file(client, f"{APP_DIR}/.env", generate_env_file(server_ip=server_ip))
    keys_injected = any([OPENAI_API_KEY, ANTHROPIC_API_KEY, GEMINI_API_KEY])
    if keys_injected:
        print("    API keys auto-injected from local .env")
    else:
        print(f"    WARNING: No API keys found! Add them to {APP_DIR}/.env on the server.")

    # Step 5: Pull images & start
    print(">>> Pulling images from GHCR and starting services...")
    run(client, f"""
set -euo pipefail
cd {APP_DIR}
docker compose down --remove-orphans 2>/dev/null || true
docker compose pull
docker compose up -d
echo ">>> Waiting for services..."
sleep 15
docker compose ps
""", timeout=600)

    # Step 6: Nginx
    print(">>> Configuring Nginx...")
    write_remote_file(client, "/etc/nginx/sites-available/agentblack", generate_nginx_config())
    run(client, """
set -euo pipefail
rm -f /etc/nginx/sites-enabled/default
ln -sf /etc/nginx/sites-available/agentblack /etc/nginx/sites-enabled/agentblack
nginx -t
systemctl enable --now nginx
systemctl reload nginx
""")

    # Step 7: Firewall
    print(">>> Configuring firewall...")
    run(client, """
ufw allow 22/tcp || true
ufw allow 80/tcp || true
ufw allow 443/tcp || true
ufw --force enable
""")

    # Step 8: SSL
    print(">>> Obtaining SSL certificate...")
    run(client, f"""
certbot --nginx -d {DOMAIN} --email {EMAIL} --agree-tos --non-interactive || true
systemctl reload nginx
""", timeout=300)

    # Step 9: Health check
    print(">>> Running health checks...")
    run(client, f"""
echo "=== Docker Status ==="
cd {APP_DIR} && docker compose ps

echo ""
echo "=== Control Panel Health ==="
curl -fsS http://127.0.0.1:{CONTROL_PANEL_PORT}/health || echo "Control panel not responding"

echo ""
echo "=== Nginx Test ==="
nginx -t 2>&1

echo ""
echo "=== SSL Certs ==="
certbot certificates 2>/dev/null || true

echo ""
echo "=== Site ==="
curl -fsS -o /dev/null -w "HTTP Status: %{{http_code}}" https://{DOMAIN}/ || echo "Site not accessible yet"
""")

    print("\n" + "=" * 60)
    print(f"  DEPLOYMENT COMPLETE!")
    print(f"  Site URL  : https://{DOMAIN}")
    print(f"  API URL   : https://{DOMAIN}/api/")
    print(f"  Server    : {USER}@{HOST}")
    print(f"  Images    : {IMAGE_BASE}/*:{IMAGE_TAG}")
    print("=" * 60)
    print(f"\n  All done! No manual steps needed.")


