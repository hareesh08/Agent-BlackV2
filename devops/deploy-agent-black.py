def write_remote_file(client, remote_path, content):
    """Write a string to a file on the remote server via SFTP."""
    sftp = client.open_sftp()
    # Ensure parent directory exists
    parent = os.path.dirname(remote_path).replace("\\", "/")
    run(client, f"mkdir -p {parent}", print_output=False)
    with sftp.open(remote_path, "w") as f:
        f.write(content)
    sftp.close()


